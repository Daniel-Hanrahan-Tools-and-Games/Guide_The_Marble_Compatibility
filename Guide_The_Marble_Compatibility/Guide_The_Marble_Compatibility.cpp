bool is_solid(int tile_id) { return tile_id >= 1 && tile_id <= 13; }

void LoadRoom(int room_idx) {
    current_room = room_idx;
    vx = 0; vy = 0;
    active_bumpers.clear();

    switch(room_idx) {
        case 0: px = 120; py = 20; active_bumpers.push_back({120, 70}); break;
        case 1: px = 180; py = 10; active_bumpers.push_back({100, 50}); active_bumpers.push_back({180, 80}); break;
        case 2: px = 140; py = 10; active_bumpers.push_back({60, 40}); active_bumpers.push_back({180, 40}); active_bumpers.push_back({120, 100}); break;
        case 3: px = 20; py = 10; active_bumpers.push_back({40, 60}); active_bumpers.push_back({200, 60}); break;
        case 4: px = 120; py = 10; active_bumpers.push_back({120, 68}); break;
        case 5: px = 40; py = 10; active_bumpers.push_back({80, 40}); active_bumpers.push_back({160, 80}); break;
        case 6: px = 200; py = 10; active_bumpers.push_back({120, 20}); active_bumpers.push_back({120, 110}); break;
        case 7: px = 120; py = 10; active_bumpers.push_back({50, 50}); active_bumpers.push_back({190, 50}); active_bumpers.push_back({120, 90}); break;
        case 8: px = 120; py = 10; active_bumpers.push_back({30, 30}); active_bumpers.push_back({210, 30}); active_bumpers.push_back({30, 100}); active_bumpers.push_back({210, 100}); break;
    }
}

// =====================
// LUA EVENT SYSTEM
// =====================
void TriggerLuaBumperEvent() {
    if (!L) return;
    lua_getglobal(L, "on_bumper_hit");
    if (lua_isfunction(L, -1)) {
        lua_pcall(L, 0, 0, 0);
    } else { lua_pop(L, 1); }
}

void CallLuaModUpdate() {
    if (!L || show_title) return;
    lua_getglobal(L, "on_update");
    if (lua_isfunction(L, -1)) {
        lua_pushinteger(L, current_room); 
        if (lua_pcall(L, 1, 0, 0) != LUA_OK) {
            std::cerr << "MOD ERROR: " << lua_tostring(L, -1) << std::endl;
            lua_pop(L, 1);
        }
    } else { lua_pop(L, 1); }
}

void InitLuaModSystem() {
    L = luaL_newstate();
    luaL_openlibs(L);
    if (luaL_dofile(L, "Guide_The_Marble_Compatibility_Mod/Guide_The_Marble_Compatibility_Mod.lua") != LUA_OK) {
        std::cerr << "LUA LOAD ERROR: " << lua_tostring(L, -1) << std::endl;
    }
}

