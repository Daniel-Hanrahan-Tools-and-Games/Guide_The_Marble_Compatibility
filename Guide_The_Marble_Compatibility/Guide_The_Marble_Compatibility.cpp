void UpdateGame() {
    if (show_title) {
        title_timer--;
        if (title_timer <= 0 || IsKeyPressed(KEY_Z)) {
            show_title = false;
            LoadRoom(GetRandomValue(0, NUM_ROOMS - 1));
        }
        return;
    }

    CallLuaModUpdate();

    if (IsKeyDown(KEY_LEFT)) vx -= H_ACCEL;
    if (IsKeyDown(KEY_RIGHT)) vx += H_ACCEL;
    vx *= H_FRICTION;
    px += vx;

    vy += GRAVITY;
    if (vy > MAX_FALL_SPEED) vy = MAX_FALL_SPEED; 
    py += vy;

    // Bumper Physics & Event
    float cx = px + 4.0f;
    float cy = py + 4.0f;
    for (auto& b : active_bumpers) {
        float dx = cx - b.x;
        float dy = cy - b.y;
        float dist = sqrtf(dx * dx + dy * dy);
        if (dist < (B_RADIUS + 4.0f) && dist > 0) {
            vx = (dx / dist) * BOUNCE_STRENGTH;
            vy = (dy / dist) * BOUNCE_STRENGTH;
            TriggerLuaBumperEvent(); // Fires the Lua function!
        }
    }

    // Wall Collision
    float checkPoints[4][2] = {{px+1, py+1}, {px+7, py+1}, {px+1, py+7}, {px+7, py+7}};
    for (int i = 0; i < 4; i++) {
        int tx = (int)(checkPoints[i][0] / TILE);
        int ty = (int)(checkPoints[i][1] / TILE);
        if (ty >= 0 && ty < 17 && tx >= 0 && tx < 30) {
            if (is_solid(map_data[current_room][ty][tx])) {
                LoadRoom(0); return; 
            }
        }
    }

    // Room Transition
    if (py > SCREEN_H) {
        int next = (current_room + 1) % NUM_ROOMS;
        LoadRoom(next);
    }
}

void DrawTic80Graphic(Texture2D tex, int id, float x, float y) {
    int sprPerRow = tex.width / 8;
    Rectangle src = {(float)((id % sprPerRow) * 8), (float)((id / sprPerRow) * 8), 8, 8};
    DrawTextureRec(tex, src, {x, y}, WHITE);
}

